    private static boolean verify(String algorithm, PublicKey publicKey, byte[] message, byte[] signature)
            throws Exception {
        Signature verifier = Signature.getInstance(algorithm, HiTls4jProvider.PROVIDER_NAME);
        verifier.initVerify(publicKey);
        verifier.update(message);
        return verifier.verify(signature);
    }

    private static byte[] xmssPublicBlob(int parameterId, byte[] seedAndRoot) {
        if (seedAndRoot.length == 0 || (seedAndRoot.length & 1) != 0) {
            throw new IllegalArgumentException("XMSS public vector must contain seed and root");
        }
        int n = seedAndRoot.length / 2;
        byte[] output = new byte[12 + seedAndRoot.length];
        putUint32Be(output, 0, XMSS_PUBLIC_BLOB_VERSION);
        putUint32Be(output, 4, parameterId);
        putUint32Be(output, 8, n);
        System.arraycopy(seedAndRoot, 0, output, 12, seedAndRoot.length);
        return output;
    }

    private static void putUint32Be(byte[] output, int offset, int value) {
        output[offset] = (byte) (value >>> 24);
        output[offset + 1] = (byte) (value >>> 16);
        output[offset + 2] = (byte) (value >>> 8);
        output[offset + 3] = (byte) value;
    }

    private static byte[] hex(String value) {
        String normalized = value.replaceAll("\\s", "");
        if ((normalized.length() & 1) != 0) {
            throw new IllegalArgumentException("Hex value has odd length");
        }
        byte[] output = new byte[normalized.length() / 2];
        for (int i = 0; i < output.length; i++) {
            int high = Character.digit(normalized.charAt(i * 2), 16);
            int low = Character.digit(normalized.charAt(i * 2 + 1), 16);
            if (high < 0 || low < 0) {
                throw new IllegalArgumentException("Invalid hex value");
            }
            output[i] = (byte) ((high << 4) | low);
        }
        return output;
    }

